    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        if (txtDato.getText().isEmpty()) {
            txtAviso.setVisible(true);
        } else {
            lblUser = txtDato.getText();
            String sql = "SELECT nombre FROM empleado WHERE id_empleado=?";
            try {
                ps = cn.prepareStatement(sql);
                ps.setString(1, lblUser);
                rs = ps.executeQuery();
                if (rs.next()) {
                    user = rs.getString("nombre");
                    JFEmployee e = new JFEmployee();
                    e.setVisible(true);
                    dispose();
                    JOptionPane.showMessageDialog(null, "Entre en empleado");
                } else {
                    user = "";
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error en el Login, revise sus datos!");
            }

            String sql1 = "SELECT nombre FROM cliente WHERE nit=?";
            try {
                ps = cn.prepareStatement(sql1);
                ps.setString(1, lblUser);
                rs = ps.executeQuery();
                if (rs.next()) {
                    user = rs.getString("nombre");
                    JFCostumer c = new JFCostumer();
                    c.setVisible(true);
                    dispose();
                    JOptionPane.showMessageDialog(null, "Entre en cliente");
                } else {
                    user = "";
                }

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error en el Login, revise sus datos!");
            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

