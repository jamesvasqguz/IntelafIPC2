    private void listarOC() {
        String sql = "SELECT * FROM pedido WHERE en_tienda=0 AND tienda_destino=?";
        model.setColumnIdentifiers(new Object[]{"ID Pedido", "Fecha", "NIT Cliente"});
        try {
            ps = cn.prepareStatement(sql);
            ps.setString(1, tienda);
            rs = ps.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("id_pedido"), rs.getString("fecha_pedido"), rs.getString("nit_cliente")});
            }
            tablaOC.setModel(model);
        } catch (SQLException e) {
            System.out.println("Error al ingresar datos a la tabla: " + e);
        }
    }

